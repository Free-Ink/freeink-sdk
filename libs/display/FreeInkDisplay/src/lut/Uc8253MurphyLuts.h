// UC8253 waveform LUTs for the Murphy M3 (CrowPanel 3.7", 240x416), transcribed
// verbatim from the manufacturer's reference ("M3 LUT.txt"). Five registers per
// bank: 0x20 VCOM, 0x21 WW, 0x22 BW, 0x23 WB, 0x24 BB.
//
// Per-register lengths follow the manufacturer header: VCOM (0x20) and BW (0x22)
// are 56 bytes (8 groups of 7); WW/WB/BB are 42 bytes (6 groups). Each group is
// 7 bytes: a level-select byte, four sub-phase frame counts, then repeat bytes.
//
// DEFAULT is the manufacturer "HALF_REFRESH" (GC) set: a multi-level, ghost-
// clearing drive. FAST is the "FAST_REFRESH" (DU) set: a quick single-direction
// drive. Both sets carry real BW/WB transition waveforms, so the driver runs a
// differential refresh (old->DTM1, new->DTM2): unchanged pixels take WW/BB,
// changed pixels take BW/WB. The driver still promotes a fast refresh to a full
// one every MURPHY_GHOST_CLEAR_INTERVAL refreshes to flush residual ghosting.

